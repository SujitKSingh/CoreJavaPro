class PrintPattern 
{
public static void main(String[] args) 
	{
	int i, j;
	for(i=5;i>=1;i--)
	{
		for(j=1;j<=i;j++)
		{
			System.out.print("*");
		}
			System.out.println();
		}
	int k, l;
	for(k=1;k<=5;k++)
	{
		for(l=1;l<=k;l++)
		{
			System.out.print("*");
		}
			System.out.println();
		}
	}
}